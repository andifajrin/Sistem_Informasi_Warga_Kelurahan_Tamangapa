    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        try {
            st = cn.createStatement();
            if (txTahun.getText().equals("") || 
                    txRemaja.getText().equals("") ||
                    txDewasa.getText().equals("") ||
                    txLansia.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!", "Validsasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            //simpan data 
            if (btSimpan.getText() == "Simpan") {
                String cek = "SELECT * FROM klasifikasi WHERE tahun = '"+txTahun.getText() + "'";
                rs = st.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Tahun yang dimasukkan sudah ada!");
                } else {
                    String sql = "INSERT INTO klasifikasi VALUES ('"+ txTahun.getText()+ 
                        "','"+ txRemaja.getText() +
                        "','"+ txDewasa.getText() +
                        "','"+ txLansia.getText() + "')";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
                Bersih();
                TampilData();
                }
            } 
            else {
                //ubah data
                String update = "UPDATE klasifikasi SET remaja = '" + txRemaja.getText() + 
                        "', dewasa = '"+ txDewasa.getText() +
                        "', lansia = '"+txLansia.getText()+
                        "' WHERE tahun ='"+txTahun.getText()+"'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
                Bersih();
                TampilData();
                        
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btSimpanActionPerformed

