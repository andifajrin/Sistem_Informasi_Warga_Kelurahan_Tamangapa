    private void TampilData(){
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM klasifikasi");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("Tahun");
            model.addColumn("Remaja");
            model.addColumn("Dewasa");
            model.addColumn("Lansia");
            
            int no = 1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("tahun"),
                    rs.getString("remaja"),
                    rs.getString("dewasa"),
                    rs.getString("lansia")
                };
                model .addRow(data);
                tbData.setModel(model);
            }
        } catch (Exception e){
            
        }
    }
    
